export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.es;

export function t(key: TranslationKey, lang: Language = 'es', replacements?: Record<string, string>): string {
  let text = translations[lang][key as keyof typeof translations.es] || translations['es'][key];

  if (replacements) {
    Object.entries(replacements).forEach(([key, value]) => {
      text = text.replace(`{${key}}`, value);
    });
  }

  return text;
}
